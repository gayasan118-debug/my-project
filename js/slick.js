    Slick.prototype.slideHandler = function(index, dontAnimate, dontAutoplay) {

        var targetSlide, slickTarget, dotIndexes, _ = this,
            oldSlide = index;

        if (_.animating === true) {
            return;
        }

        if (_.options.fade === true && _.currentSlide === index) {
            return;
        }

        if (_.slideCount <= _.options.slidesToShow) {
            return;
        }

        if (dontAnimate === false) {
            _.asNavFor(index);
        }

        targetSlide = oldSlide;

        if (_.options.infinite === true) {
            if (oldSlide < 0) {
                slickTarget = oldSlide + _.slideCount;
                if (!dontAnimate) {
                    _.$slideTrack.css({
                        left: _.getLeft(slickTarget)
                    });
                    _.$slideTrack.css({
                        opacity: 1
                    });
                }
                oldSlide = slickTarget;
            } else if (oldSlide >= _.slideCount) {
                slickTarget = oldSlide - _.slideCount;
                if (!dontAnimate) {
                    _.$slideTrack.css({
                        left: _.getLeft(slickTarget)
                    });
                    _.$slideTrack.css({
                        opacity: 1
                    });
                }
                oldSlide = slickTarget;
            }
        }

        _.animating = true;

        _.$slider.trigger('beforeChange', [_, _.currentSlide, oldSlide]);

        targetSlick = _.getLeft(oldSlide);

        _.currentSlide = oldSlide;

        _.setSlideClasses(_.currentSlide);

        _.updateDots();
        _.updateArrows();

        if (_.options.fade === true) {
            _.fadeSlideOut(targetSlide);
            _.fadeSlide(oldSlide, function() {
                _.postSlide(oldSlide);
            });
        } else {
            _.animateSlide(targetSlick, function() {
                _.postSlide(oldSlide);
            });
        }

        if (dontAutoplay !== true) {
            _.autoPlay();
        }

    };

