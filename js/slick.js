    Slick.prototype.swipeEnd = function(event) {

        var _ = this,
            slideCount;

        _.dragging = false;
        _.interrupted = false;
        _.shouldClick = (_.touchObject.swipeLength > _.touchObject.minSwipe) ? false : true;

        if (_.touchObject.curX === undefined) {
            return false;
        }

        if (_.touchObject.edgeHit === true) {
            _.$slider.trigger('edge', [_, _.touchObject.direction]);
        }

        if (_.touchObject.swipeLength >= _.touchObject.minSwipe) {

            verticalSwipeLength = _.touchObject.swipeVertical;

            if (_.options.vertical === true && verticalSwipeLength === true) {
                slideCount = _.touchObject.curY > 0 ?
                    _.currentSlide - _.options.slidesToScroll : _.currentSlide + _.options.slidesToScroll;
                _.slideHandler(slideCount);
            } else if (_.options.vertical === false && verticalSwipeLength === false) {
                slideCount = _.touchObject.curX > 0 ?
                    _.currentSlide - _.options.slidesToScroll : _.currentSlide + _.options.slidesToScroll;
                _.slideHandler(slideCount);
            }
        } else {
            // If the user just clicks, we want the normal click behavior to occur.
            // If the above returns true it will disable the normal click behavior.
            _.slideHandler(_.currentSlide);
        }

        _.autoPlay();

        _.touchObject = {};
    };

    Slick.prototype.swipeMove = function(event) {

        var _ = this,
            curLeft,
            swipeDirection,
            swipeLength,
            verticalSwipeLength;

        curLeft = event.touches !== undefined ? event.touches[0].pageX : event.clientX;
        startY = event.touches !== undefined ? event.touches[0].pageY : event.clientY;

        if (!_.touchObject.startX) {
            return false;
        }

        swipeDirection = _.getSwipeDirection(curLeft, startY);

        swipeLength = Math.round(Math.sqrt(
            Math.pow(curLeft - _.touchObject.startX, 2)));

        verticalSwipeLength = Math.round(Math.sqrt(
            Math.pow(startY - _.touchObject.startY, 2)));

        if (!_.options.verticalSwiping && verticalSwipeLength > 4) {
            return false;
        }

        if (_.options.vertical === true) {
            swipeLength = verticalSwipeLength;
        }

        _.touchObject.swipeLength = swipeLength;
        _.touchObject.swipeVertical = verticalSwipeLength;

        if (swipeDirection === 'vertical') {
            return;
        }

        if (event.originalEvent !== undefined && _.touchObject.fingerCount === 1) {
            event.preventDefault();
        }

        edgeLeft = _.options.rtl === true ? _.slideCount - 1 - _.currentSlide : _.currentSlide;
        edgeRight = _.options.rtl === true ? _.currentSlide : _.slideCount - 1 - _.currentSlide;

        if (_.options.infinite === false) {
            if ((edgeLeft === 0 && swipeDirection === 'right') ||
                (edgeRight === 0 && swipeDirection === 'left')) {
                _.touchObject.swipeLength = swipeLength * _.options.edgeFriction;
            }
        }

        if (_.options.vertical === false) {
            _.swipeLeft = _.getLeft(_.currentSlide);
            _.touchObject.curX = curLeft;
            _.touchObject.swipeLength = swipeLength;

            _.touchObject.elemLeft = _.swipeLeft + ((curLeft > _.touchObject.startX) ?
                (swipeLength * 1) : (swipeLength * -1));

            if (_.options.rtl === true) {
                _.touchObject.elemLeft = _.swipeLeft - ((curLeft > _.touchObject.startX) ?
                    (swipeLength * 1) : (swipeLength * -1));
            }

            if (_.options.infinite === true) {
                if ((_.touchObject.curX > _.touchObject.startX && edgeLeft === 0)) {
                    _.touchObject.elemLeft = ((curLeft - _.touchObject.startX) * _.options.edgeFriction) + _.swipeLeft;
                } else if ((_.touchObject.curX < _.touchObject.startX && edgeRight === 0)) {
                    _.touchObject.elemLeft = ((curLeft - _.touchObject.startX) * _.options.edgeFriction) + _.swipeLeft;
                }
            }

            if (_.options.rtl === true) {
                _.$slideTrack.css({ left: _.touchObject.elemLeft });
            } else {
                _.$slideTrack.css({ left: _.touchObject.elemLeft });
            }
        } else {
            _.swipeLeft = _.getLeft(_.currentSlide);
            _.touchObject.curY = startY;
            _.touchObject.swipeLength = swipeLength;

            _.touchObject.elemTop = _.swipeLeft + ((startY > _.touchObject.startY) ?
                (swipeLength * 1) : (swipeLength * -1));

            if (_.options.infinite === true) {
                if ((_.touchObject.curY > _.touchObject.startY && edgeLeft === 0)) {
                    _.touchObject.elemTop = ((startY - _.touchObject.startY) * _.options.edgeFriction) + _.swipeLeft;
                } else if ((_.touchObject.curY < _.touchObject.startY && edgeRight === 0)) {
                    _.touchObject.elemTop = ((startY - _.touchObject.startY) * _.options.edgeFriction) + _.swipeLeft;
                }
            }

            _.$slideTrack.css({ top: _.touchObject.elemTop });
        }
    };

